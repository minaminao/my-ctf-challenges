def dfs_all(current_path):
    TARGET_LENGTH = 17

    if len(current_path) == TARGET_LENGTH:
        print("CBC{" + current_path + "}")
        return
    if len(current_path) > TARGET_LENGTH:
        return
    
    current_char_idx = ord(current_path[-1]) - ord('a')
    
    for next_char_idx in range(26):
        if v[current_char_idx][next_char_idx] == 1:
            next_char = chr(next_char_idx + ord('a'))
            dfs_all(current_path + next_char)

for i in range(26):
    dfs_all(chr(i + ord('a')))
